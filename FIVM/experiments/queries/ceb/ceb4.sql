IMPORT DTREE FROM FILE 'ceb4.txt';

CREATE STREAM AN(A long, B long) FROM FILE 'an.dat' LINE DELIMITED CSV;
CREATE STREAM CI(B long, C long) FROM FILE 'ci.dat' LINE DELIMITED CSV;
CREATE STREAM MI(D long, C long) FROM FILE 'mi.dat' LINE DELIMITED CSV;
CREATE STREAM MII1(E long, C long) FROM FILE 'mii1.dat' LINE DELIMITED CSV;
CREATE STREAM MII2(G long, C long) FROM FILE 'mii2.dat' LINE DELIMITED CSV;
CREATE STREAM MK(H long, C long) FROM FILE 'mk.dat' LINE DELIMITED CSV;
CREATE STREAM PI(I long, B long) FROM FILE 'pi.dat' LINE DELIMITED CSV;

SELECT SUM(1) FROM AN NATURAL JOIN CI NATURAL JOIN MI NATURAL JOIN MII1 NATURAL JOIN MII2 NATURAL JOIN MK
    NATURAL JOIN PI;
